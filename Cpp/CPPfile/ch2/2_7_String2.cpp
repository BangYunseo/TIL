#include <iostream>
#include <string>
#define _CRT_NO_SECURE_WARNINGS

using namespace std;

int main(void) {
	string song("Falling in love with you");
	string elvis("Elvis Presley");
	string singer;

	cout << song << "를 부른 가수는?(힌트 : 첫 글자는 " << elvis[0] << ") >> ";
	getline(cin, singer);
	if (elvis == singer)
		cout << "정답입니다." << endl;
	else
		cout << "오답입니다. " << elvis << " 입니다." << endl;
	return 0;
}